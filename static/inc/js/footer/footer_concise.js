$j(document).ready(function () {
    $j('body').bind('click', function (event) {
        if ($j('#footerCsInfoPop').css("display") != "none") {
            $j('#footerCsInfoPop').hide();
        }
        if ($j('#yFamDl dd').css("display") != "none") {
            $j('#yFamDl dd').hide();
        }
        if ($j('#yFamDl dt').css("display") != "none") {
            $j('#yFamDl dt').attr("class", "");
        }
    });

    $j('#yFamDl dt a').bind('focus', function (event) {
        $j('#yFamDl dt').attr("class", "on");
        $j(this).parent().next().show();
    });
    $j('#yFamDl dt').bind('click', function (event) {
        if ($j('#yFamDl dt').attr("class") == "on") {
            $j('#yFamDl dt').attr("class", "");
            $j('#yFamDl dd').hide();
        } else {
            $j('#yFamDl dt').attr("class", "on");
            $j('#yFamDl dd').show();
        }
        event.stopPropagation();
    });
    $j('#corpNumLnk, #yMnuLi li a').bind('focus', function (event) {
        $j('#yFamDl dd').hide();
        $j('#yFamDl dt').attr("class", "");
    });
    /* Footer 계열사 e */
    /* 상담시간 안내 s */
    $j('#footerWrap .csFAQ .btnCs3').bind('click', function (event) {
        $j('#footerCsInfoPop').show();
        event.stopPropagation();
    });
    $j('#footerCsInfoPop .btnCloseCsInfo').bind('click', function (event) {
        $j('#footerCsInfoPop').hide();
    });
    /* 상담시간 안내 e */

    $j("#mobileLink").bind('click', function () {
        setCookie('USEPC', 'N');
        var moveUrl = "http://m.yes24.com/?isDeviceRedirect=y";
        var url = location.href;
        if (url.indexOf('movie.yes24.com') > -1) {
            moveUrl = "http://m.movie.yes24.com/";
        } else if (url.indexOf('ticket.yes24.com') > -1) {
            moveUrl = "http://m.ticket.yes24.com/";
        } else if (url.indexOf('estory.yes24.com') > -1) {
            moveUrl = "http://m.estory.yes24.com/";
        }

        document.location = moveUrl;
    });


});

